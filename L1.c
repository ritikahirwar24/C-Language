#include <stdio.h>

int main() {
    // Declaring and initializing variables
    int age = 25;
    float height = 5.9;
    char grade = 'A';
    char name[] = "Alice";  // character array for a string

    // Printing the variables
    printf("Name: %s\n", name);
    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Grade: %c\n", grade);

    return 0;
}
